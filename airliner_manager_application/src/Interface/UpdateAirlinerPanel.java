    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) tblAirPlane.getModel();
        dtm.setRowCount(0);

        for (Airplane a : this.airliner.getFleet().getLstAirplane()) {
            Object[] row = new Object[3];
            row[0] = a;
            row[1] = a.getModel();
            row[2] = a.getIsAvailable();
            dtm.addRow(row);
        }

    }

