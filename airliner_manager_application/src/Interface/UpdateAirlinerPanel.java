    private void btnUpdateAirlinerNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateAirlinerNameActionPerformed
        if (txtAirlinerName.getText().trim()!="") {
            this.airliner.setAirlinerName(txtAirlinerName.getText());
            JOptionPane.showMessageDialog(null, "Update Successful.");
        } else {
            JOptionPane.showMessageDialog(null, "Please enter valid airliner name.");
            return;
        }

    }//GEN-LAST:event_btnUpdateAirlinerNameActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        this.containerPanel.remove(this);
        CardLayout layout = (CardLayout) this.containerPanel.getLayout();

        Component[] comps = this.containerPanel.getComponents();
        for (Component comp : comps) {
            if (comp instanceof manageAirlinerPanel) {
                manageAirlinerPanel ma = (manageAirlinerPanel) comp;
                ma.populateTable();
            }
        }
        layout.previous(containerPanel);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnDeleteAirplaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAirplaneActionPerformed
        int selectedRow = this.tblAirPlane.getSelectedRow();
        if (selectedRow >= 0) {
            this.airliner.getFleet().getLstAirplane().remove((Airplane) this.tblAirPlane.getValueAt(selectedRow, 0));
            JOptionPane.showMessageDialog(null, "Airplane Deleted.");
        } else {
            JOptionPane.showMessageDialog(null, "Please select airplane to be deleted");
            return;
        }
        populate();
    }//GEN-LAST:event_btnDeleteAirplaneActionPerformed

    private void btnAddAirplaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAirplaneActionPerformed
        AirplaneCreate_update ac = new AirplaneCreate_update(this.containerPanel, this.airliner, null, "CREATE");
        this.containerPanel.add("AirplaneCreate_update", ac);
        CardLayout layout = (CardLayout) this.containerPanel.getLayout();
        layout.next(this.containerPanel);
    }//GEN-LAST:event_btnAddAirplaneActionPerformed

    private void btnUpdateAirplaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateAirplaneActionPerformed
        int selectedRow = this.tblAirPlane.getSelectedRow();
        if (selectedRow >= 0) {
            AirplaneCreate_update au = new AirplaneCreate_update(this.containerPanel, this.airliner,(Airplane)this.tblAirPlane.getValueAt(selectedRow, 0) , "UPDATE");
            this.containerPanel.add("AirplaneCreate_update", au);
            CardLayout layout = (CardLayout) this.containerPanel.getLayout();
            layout.next(this.containerPanel);
        } else {
            JOptionPane.showMessageDialog(null, "Please select airplane to be updated");
        }
    }//GEN-LAST:event_btnUpdateAirplaneActionPerformed


